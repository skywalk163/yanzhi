"""
言知·动词白名单与语义映射
管理高层语义动词到底层函数/动词的映射。

白名单中的动词会在言律层优先匹配，
匹配到的动词被映射为言知标准库中的函数调用。

示例：
  "打开灯" → 灯.开()
  "查温度" → 温度传感器.读取()
"""
from typing import Dict, Callable, Any


# 动词白名单（领域专用语义映射）
ACTION_VOCAB = {
    # 设备控制
    '打开': '开',
    '开启': '开',
    '启动': '开',
    '关闭': '关',
    '关掉': '关',
    '停止': '停',
    
    # 感知
    '查看': '读取',
    '查': '读取',
    '检测': '测',
    '识别': '识',
    
    # 通信
    '发送': '发',
    '通知': '告',
    '提醒': '告',
    
    # 状态
    '设置': '赋值',
    '设定': '定义',
    '切换': '换',
}


def resolve_action(verb: str) -> str:
    """将高层语义动词解析为言知内核动词"""
    return ACTION_VOCAB.get(verb, verb)
